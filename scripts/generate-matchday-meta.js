#!/usr/bin/env node
/** Writes client/src/generated-matchday-meta.js from schedule kickoffs. */
const fs = require('fs');
const path = require('path');

const { MATCHDAY_ORDER, MATCHDAY_META } = require('../server/data/wc2026-schedule');

const out = path.join(__dirname, '../client/src/generated-matchday-meta.js');
const body = `// Auto-generated by scripts/generate-matchday-meta.js — do not edit
export const MATCHDAY_ORDER = ${JSON.stringify(MATCHDAY_ORDER, null, 2)};

export const MATCHDAY_META = ${JSON.stringify(MATCHDAY_META, null, 2)};
`;

fs.writeFileSync(out, body);
console.log('Wrote', path.relative(process.cwd(), out));
